//2wheel
public abstract class TwoWheeler extends Vehicle
{
	
	protected String model;
	protected int weight;
	
	public TwoWheeler(String manufacturer, int numWheels, String model, int weight) 
	{
		super(manufacturer, numWheels);
		
		
		this.model = model;
		this.weight = weight;
	}
	
	//get

	protected String getClassName() 
	{
		return "TwoWheeler";
	}
	
	protected String getModel() 
	
	{
		return model;
	}



	protected int getWeight() 
	{
		return weight;
	}
	
	//set

	protected void setWeight(int weight)
	{
		this.weight = weight;
	}
	
	protected void setModel(String model) 
	{
		this.model = model;
	}

	
	public String toString() 
	{
		return "TwoWheeler [manufacturer=" + manufacturer + ", numWheels=" + numWheels + ", model=" + model + ", weight=" + weight + "]";
	}
	
}
